package Algoritmos;

public class ForReversoPraImprimirString {

	 public static void main(String[] args) {
		 
	        
	        int n = 4;
	        
	        int arr[] = new int[]{1,4,3,2};
	        
	        for(int i = n-1; i >= 0; i--){
	            System.out.print(arr[i] + " ");
	        }
	    }

}
